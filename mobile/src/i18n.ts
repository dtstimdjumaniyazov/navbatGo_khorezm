export type TKey = keyof typeof TEXTS.ru;

export function t(lang: Lang, key: TKey, params?: Record<string, string | number>): string {
  let text: string = TEXTS[lang][key] ?? TEXTS.ru[key];
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

/** Локализованное поле данных: uz-вариант, если выбран uz и он заполнен. */
export function locText(lang: Lang, ru: string, uz: string): string {
  return lang === 'uz' && uz ? uz : ru;
}
